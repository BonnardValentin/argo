def _make_artifact(
    path: Path, rel: Path, full_text: str, blocks: list[CommentBlock]
) -> RawArtifact:
    """Assemble a RawArtifact whose body shows each block with its line
    range and the symbol it attaches to (when detectable)."""
    sections: list[str] = [f"File: {rel.as_posix()}"]
    lang = path.suffix.lower().lstrip(".")
    sections.append(f"Language: {lang}")
    sections.append("")

    for b in blocks:
        header = f"— lines {b.start}-{b.end} ({b.kind})"
        if b.following_symbol:
            header += f"  →  {b.following_symbol}"
        sections.append(header)
        sections.append(b.body.strip())
        sections.append("")

    body = "\n".join(sections).strip()

    try:
        mtime = datetime.fromtimestamp(path.stat().st_mtime, tz=timezone.utc)
    except OSError:
        mtime = None

    total_lines = sum(b.end - b.start + 1 for b in blocks)
    file_lines = full_text.count("\n") + 1

    return RawArtifact(
        source=Source(
            kind="local",
            url=None,
            ref=rel.as_posix(),
            fetched_at=datetime.now(timezone.utc),
        ),
        title=rel.as_posix(),
        body=body,
        author=None,
        created_at=mtime,
        extras={
            "path": str(path),
            "language": lang,
            "blocks": len(blocks),
            "comment_lines": total_lines,
            "file_lines": file_lines,
        },
    )


def _extract_blocks(text: str, rules: dict) -> Iterator[CommentBlock]:
    """Walk `text` line-by-line, yielding coalesced comment blocks per rules."""
    lines = text.splitlines()
    n = len(lines)
    i = 0

    line_marker: str | None = rules.get("line")
    block = rules.get("block")
    block_open, block_close = (block[0], block[1]) if block else (None, None)
    doc = rules.get("doc")
    doc_open, doc_close = (doc[0], doc[1]) if doc else (None, None)
    outer_doc: str | None = rules.get("outer_doc")

    def symbol_after(idx: int) -> str | None:
        # Peek the next 3 non-blank non-comment lines; use the first one
        # as a hint about what the comment above was describing.
        for k in range(idx, min(idx + 5, n)):
            ln = lines[k].strip()
            if not ln:
                continue
            if line_marker and ln.startswith(line_marker):
                continue
            if block_open and ln.startswith(block_open):
                continue
            if outer_doc and ln.startswith(outer_doc):
                continue
            return ln[:100]
        return None

    while i < n:
        line = lines[i]
        stripped = line.lstrip()

        # Doc block first (distinguishes /** … */ from /* … */)
        if doc_open and stripped.startswith(doc_open) and doc_close:
            start = i
            rest = stripped[len(doc_open) :]
            if doc_close in rest:
                body = rest[: rest.rfind(doc_close)]
                i += 1
                yield CommentBlock(
                    start + 1, i, "doc", _clean_block(body), symbol_after(i)
                )
                continue
            buf = [rest]
            i += 1
            while i < n and doc_close not in lines[i]:
                buf.append(lines[i])
                i += 1
            if i < n:
                tail = lines[i]
                buf.append(tail[: tail.find(doc_close)])
                i += 1
            yield CommentBlock(
                start + 1, i, "doc", _clean_block("\n".join(buf)), symbol_after(i)
            )
            continue

        # Plain block comment
        if block_open and stripped.startswith(block_open) and block_close:
            start = i
            rest = stripped[len(block_open) :]
            if block_close in rest:
                body = rest[: rest.rfind(block_close)]
                i += 1
                yield CommentBlock(
                    start + 1, i, "block", _clean_block(body), symbol_after(i)
                )
                continue
            buf = [rest]
            i += 1
            while i < n and block_close not in lines[i]:
                buf.append(lines[i])
                i += 1
            if i < n:
                tail = lines[i]
                buf.append(tail[: tail.find(block_close)])
                i += 1
            yield CommentBlock(
                start + 1, i, "block", _clean_block("\n".join(buf)), symbol_after(i)
            )
            continue

        # Outer-doc lines (Rust `///`, coalesce consecutive)
        if outer_doc and stripped.startswith(outer_doc):
            start = i
            buf = [stripped[len(outer_doc) :].strip()]
            i += 1
            while i < n and lines[i].lstrip().startswith(outer_doc):
                buf.append(lines[i].lstrip()[len(outer_doc) :].strip())
                i += 1
            yield CommentBlock(
                start + 1, i, "outer_doc", "\n".join(buf).strip(), symbol_after(i)
            )
            continue

        # Plain line comments — coalesce a contiguous run into one "group"
        if line_marker and stripped.startswith(line_marker):
            # Ignore shebangs
            if stripped.startswith("#!") and i == 0:
                i += 1
                continue
            start = i
            buf = [stripped[len(line_marker) :].strip()]
            i += 1
            while i < n and lines[i].lstrip().startswith(line_marker):
                buf.append(lines[i].lstrip()[len(line_marker) :].strip())
                i += 1
            yield CommentBlock(
                start + 1, i, "line_group", "\n".join(buf).strip(), symbol_after(i)
            )
            continue

        i += 1


def _clean_block(body: str) -> str:
    """Strip leading `*` or whitespace from block-comment interior lines."""
    out: list[str] = []
    for raw in body.splitlines():
        s = raw.strip()
        if s.startswith("*") and not s.startswith("*/"):
            s = s[1:].strip()
        out.append(s)
    # Drop leading/trailing blank lines, preserve internal spacing.
    while out and not out[0]:
        out.pop(0)
    while out and not out[-1]:
        out.pop()
    return "\n".join(out).strip()


def _is_substantial(body: str, min_chars: int, min_lines: int) -> bool:
    text = body.strip()
    if len(text) >= min_chars:
        return True
    non_trivial = [line for line in text.splitlines() if len(line.strip()) >= 20]
    return len(non_trivial) >= min_lines


def _is_noise(body: str) -> bool:
    return any(p.search(body) for p in NOISE_PATTERNS)

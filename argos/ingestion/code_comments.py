"""Generic comment extraction across many source-file languages.

The idea: any .ts / .py / .yml / .tf / .go / .rs / … file might carry
intent in its comments — module-level headers, JSDoc blocks, TODO
paragraphs, tribal-knowledge asides. We pull those out via a small
per-extension rules table (one dict entry = one new language supported)
and pass them to the same Claude extractor the docs ingester uses.

Scope of this module is deliberately narrow:
- extract substantive comments from files
- drop license boilerplate, lint pragmas, and tiny chunks
- emit one `RawArtifact` per file (or per block, with --per-block)

Everything else — extraction quality, deduping, linking — happens
downstream in the existing pipeline.
"""

from __future__ import annotations

import re
from collections.abc import Iterator
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path

from argos.models import RawArtifact, Source


# Per-extension comment-syntax rules. Adding a language = one entry.
#
# Each entry may declare:
#   line:       prefix for single-line comments
#   block:      (open, close) tuple for multi-line comments
#   doc:        (open, close) for doc-style blocks (higher signal)
#   outer_doc:  line prefix for outer doc comments (Rust's `///`)
COMMENT_RULES: dict[str, dict] = {
    # C-family
    ".ts":   {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".tsx":  {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".js":   {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".jsx":  {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".mjs":  {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".cjs":  {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".go":   {"line": "//", "block": ("/*", "*/")},
    ".rs":   {"line": "//", "block": ("/*", "*/"), "outer_doc": "///"},
    ".c":    {"line": "//", "block": ("/*", "*/")},
    ".h":    {"line": "//", "block": ("/*", "*/")},
    ".cc":   {"line": "//", "block": ("/*", "*/")},
    ".cpp":  {"line": "//", "block": ("/*", "*/")},
    ".hpp":  {"line": "//", "block": ("/*", "*/")},
    ".cs":   {"line": "//", "block": ("/*", "*/")},
    ".java": {"line": "//", "block": ("/*", "*/")},
    ".kt":   {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".swift": {"line": "//", "block": ("/*", "*/"), "doc": ("/**", "*/")},
    ".scala": {"line": "//", "block": ("/*", "*/")},
    ".dart": {"line": "//", "block": ("/*", "*/")},
    # Hash-style
    ".py":   {"line": "#", "block": ('"""', '"""')},
    ".rb":   {"line": "#"},
    ".sh":   {"line": "#"},
    ".bash": {"line": "#"},
    ".zsh":  {"line": "#"},
    ".fish": {"line": "#"},
    ".ps1":  {"line": "#"},
    ".yml":  {"line": "#"},
    ".yaml": {"line": "#"},
    ".toml": {"line": "#"},
    ".conf": {"line": "#"},
    ".ini":  {"line": ";"},
    # HCL
    ".tf":   {"line": "#", "block": ("/*", "*/")},
    ".hcl":  {"line": "#", "block": ("/*", "*/")},
    # SQL / Lua
    ".sql":  {"line": "--", "block": ("/*", "*/")},
    ".lua":  {"line": "--", "block": ("--[[", "]]")},
    # Markup
    ".html": {"block": ("<!--", "-->")},
    ".xml":  {"block": ("<!--", "-->")},
    ".svg":  {"block": ("<!--", "-->")},
}


# Noise patterns: if any match a comment's body, the block is skipped.
# Covers license preambles, lint pragmas, codegen notices, decorators-as-
# comments. Kept conservative — better to pass noise to the extractor (which
# will also filter) than to drop real signal.
NOISE_PATTERNS = [
    re.compile(r"copyright\s+(?:\(c\)\s*)?\d{4}", re.IGNORECASE),
    re.compile(r"licensed\s+under\s+the", re.IGNORECASE),
    re.compile(r"spdx-license-identifier", re.IGNORECASE),
    re.compile(r"all\s+rights\s+reserved", re.IGNORECASE),
    re.compile(r"auto-generated|autogenerated|do\s*not\s*edit", re.IGNORECASE),
    re.compile(r"this\s+file\s+was\s+generated", re.IGNORECASE),
    re.compile(
        r"eslint-(disable|enable)|@ts-(ignore|nocheck|expect-error)|prettier-ignore",
        re.IGNORECASE,
    ),
    re.compile(r"(?:^|\s)(?:noqa|type:\s*ignore|pylint:\s*disable)", re.IGNORECASE),
    re.compile(r"istanbul\s+ignore|c8\s+ignore", re.IGNORECASE),
]


# Paths (directory names) always skipped.
DEFAULT_EXCLUDES: frozenset[str] = frozenset(
    {
        ".git",
        ".svn",
        ".hg",
        "node_modules",
        ".venv",
        "venv",
        "env",
        "dist",
        "build",
        "target",
        "out",
        "__pycache__",
        ".pytest_cache",
        ".mypy_cache",
        ".ruff_cache",
        "coverage",
        ".coverage",
        ".next",
        ".turbo",
        ".nuxt",
        ".cache",
        ".parcel-cache",
        "vendor",
        "third_party",
        "third-party",
        "bower_components",
    }
)


@dataclass
class CommentBlock:
    start: int  # 1-indexed
    end: int
    kind: str  # "line_group" | "block" | "doc" | "outer_doc"
    body: str
    following_symbol: str | None = None  # hint: next non-blank non-comment line


@dataclass
class ExtractedFile:
    path: Path
    rel: Path
    blocks: list[CommentBlock] = field(default_factory=list)
    size: int = 0


class CommentExtractor:
    """Walk a repository tree, yield `RawArtifact` per source file whose
    comments carry substance. Language-agnostic via the rules table."""

    def __init__(
        self,
        root: Path,
        *,
        excludes: frozenset[str] = DEFAULT_EXCLUDES,
        extensions: tuple[str, ...] | None = None,
        per_block: bool = False,
        min_chars: int = 120,
        min_lines: int = 3,
    ) -> None:
        self.root = root.resolve()
        self.excludes = excludes
        self.extensions = extensions or tuple(COMMENT_RULES.keys())
        self.per_block = per_block
        self.min_chars = min_chars
        self.min_lines = min_lines

    def iter_files(self) -> Iterator[Path]:
        for path in sorted(self.root.rglob("*")):
            if not path.is_file():
                continue
            try:
                rel = path.relative_to(self.root)
            except ValueError:
                continue
            if any(part in self.excludes for part in rel.parts):
                continue
            if path.suffix.lower() not in self.extensions:
                continue
            yield path

    def iter_artifacts(
        self, *, max_items: int | None = None
    ) -> Iterator[RawArtifact]:
        count = 0
        for path in self.iter_files():
            rules = COMMENT_RULES.get(path.suffix.lower())
            if not rules:
                continue
            try:
                text = path.read_text(encoding="utf-8", errors="replace")
            except OSError:
                continue

            rel = path.relative_to(self.root)
            blocks = list(_extract_blocks(text, rules))
            kept = [
                b
                for b in blocks
                if _is_substantial(b.body, self.min_chars, self.min_lines)
                and not _is_noise(b.body)
            ]
            if not kept:
                continue

            if self.per_block:
                for block in kept:
                    yield _make_artifact(path, rel, text, [block])
                    count += 1
                    if max_items and count >= max_items:
                        return
            else:
                yield _make_artifact(path, rel, text, kept)
                count += 1
                if max_items and count >= max_items:
                    return


